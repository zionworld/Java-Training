package learn;

public class CalcMethods {
	
	public int sum(int a, int b){
		int c = a + b;
		return c;	
		
	}
	
	public int subs(int a, int b){
		int c = a - b;
		return c;
	
	}
	
	public int multi(int a, int b){
		int c = a * b;
		return c;		
	}
	
	public int divide(int a, int b){
		int c = a/b;
		return c;		
	}

}
